            <div class="doc_box help">
                <p>Wir möchten Ihnen nie etwas, das Ihnen nicht richtig passt, schicken. Bitte nutzen Sie diese Größentabelle, um richtige Größe zu wählen. Größentabelle bietet Ihnen internationale Größenumrechnung, was auch immer aus Großbritannien, Europa oder USA ist. Die meisten Größe auf unserer Website sind manuell gemesst, es könnte Abweichung in 2 cm haben, die abhängig von der unterschiedlichen Materialen ist. Wenn Sie eine spezielle Größe Frage haben oder weitere Informationen wünschen, kontaktieren Sie bitte unsere Kundendienstabteilung.</p>
                <p style="float:right;"><img src="/images/docs/size_guide_body.gif" alt="" /></p>
                <h5>Wie messen Sie Ihre Größe:</h5>
                <p>
                    a) Brust: Messen Sie direkt um den vollsten Teil Ihrer Büste, indem Sie einen nichtgepolsterten Büstenhalter tragen<br />
                    b) Unter der Brust: Wenn Sie für einen BH suchen, sollten Sie eine andere Maßnahme direkt unter der Brust nehmen<br />
                    c) Taille: Messen Sie um die natürliche Taille<br />
                    d) Gesäßumfang: messen an der breitesten Teil<br />
                    e) Innenbeinlänge: Messen Sie die Entfernung vom Schritt bis zum Knöchel.
                </p>
